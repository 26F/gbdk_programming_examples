// function prototypes
void init();
void checkInput();
UINT8 collisionCheck(UINT8, UINT8, UINT8, UINT8);

UINT8 player[2]; // x,y positions for player
UINT8 enemy[2]; // position of enemy

unsigned char noWalk[2];

void main() {
	init(); // init game boy stuff
	
	HIDE_WIN;
	SHOW_SPRITES;
	SHOW_BKG;

	while (1) { // main loop
		checkInput();		// Check for user input (and act on it)
		wait_vbl_done();		// Wait until VBLANK to avoid corrupting memory
	}
}

void init() {
	DISPLAY_ON; // turn the display on
	set_bkg_data(0, 2, tiles); // load the 23 tiles into memory
	set_bkg_tiles(0,0,20,18, dungeon); 
	set_sprite_data(0, 2, sprites); // load the sprites

	set_sprite_tile(0,0); // Set the first movable sprite 0 to be the first tile
	set_sprite_tile(1,1); // enemy sprite. Sprite 1 gets next sprite after 0
	
	player[0] = 64;
	player[1] = 64;

	enemy[0] = 45;
	enemy[1] = 45;
}
void checkInput() {

	if (joypad() & J_UP) { // This code will probably need optimizing...
		if (collisionCheck(player[0], player[1] - 1, enemy[0], enemy[1]) != 1) {
			player[1]--; set_sprite_tile(0, 1);
		}
		else 
		{
			set_sprite_tile(0, 0);
		}
	}
	if (joypad() & J_DOWN) { 
		if (collisionCheck(player[0], player[1] + 1, enemy[0], enemy[1]) != 1) {
			player[1]++; set_sprite_tile(0, 1);
		} 
		else 
		{
			set_sprite_tile(0, 0);
		}
	}
	if (joypad() & J_LEFT) {
		if (collisionCheck(player[0] - 1, player[1], enemy[0], enemy[1]) != 1) {
			player[0]--; set_sprite_tile(0, 1);
		} 
		else 
		{
			set_sprite_tile(0, 0);
		}
	}
	if (joypad() & J_RIGHT) { 
		if (collisionCheck(player[0] + 1, player[1], enemy[0], enemy[1]) != 1) {
			player[0]++; set_sprite_tile(0, 1);
		}
		else 
		{
			set_sprite_tile(0, 0);
		}
	}
	move_sprite(0, player[0], player[1]);
	move_sprite(1, enemy[0], enemy[1]); 
	
}

UINT8 collisionCheck(UINT8 aL, UINT8 aT, UINT8 bL, UINT8 bT) {
	return ( (aL < (bL+8)) && ((aL+8) > bL) && (aT < (bT+8)) && ((aT+8) > bT) );
}
