unsigned char cdatr[1024] = {0};

UINT8 shipx = 80;
UINT8 shipy = 132;
UINT8 bkx = 0;
UINT8 bky = 0;
void mship() {
  if (joypad() & J_UP) {
    bky--;
  }
  if (joypad() & J_DOWN) { 
    bky++;
  }
  if (joypad() & J_LEFT) {
    if (shipx > 9) {
      shipx -= 2;
    }
    bkx--;
  }
  if (joypad() & J_RIGHT) {
    if (shipx < 152) {
      shipx += 2;
    }
    bkx++;
  }
  SCY_REG = bky;
  SCX_REG = bkx;
  set_sprite_tile(0,0);
   // actually puts 0,1 because it's in 8x16 mode.
  move_sprite(0,shipx,shipy);
  set_sprite_tile(1,2); // because it's in 8x16 mode we put 2 instead of 1...
  move_sprite(1,shipx+8,shipy);

}

UINT8 fire = 0;
UINT8 ly = 0;
UINT8 lx = 0;
void main() {
  // background
  set_bkg_palette(0,2,paldat);
  set_bkg_data(0,0,dot);
  VBK_REG = 1;
  set_bkg_tiles(0,0,32,32,cdatr);
  VBK_REG = 0;
  set_bkg_tiles(0,0,32,32, dotm);
  // sprites
  set_sprite_palette(0,2,paldat);
  SPRITES_8x16;
  set_sprite_data(0,4,sprite);
  set_sprite_prop(0,1);
  set_sprite_prop(1,1);
  // laser sprite
  set_sprite_data(4,2,lasr);
  set_sprite_tile(3,4); 
  move_sprite(3,80,72);
  DISPLAY_ON;
  SHOW_BKG;
  SHOW_SPRITES;
  while (1) {
    mship();
    if (joypad() & J_A && fire == 0) {
      fire = 1;
    }
    if (fire == 1) {
      ly -= 6;
      if (ly > 160) {
        fire = 0;
        lx = shipx+4;
        ly = shipy+7;
      }
    } else {
      lx = shipx+4;
      ly = shipy+7;
    }
    move_sprite(3,lx,ly);
    wait_vbl_done();
  }

}