void main() {
	UINT8 changet = 0;
	SPRITES_8x16; // set sprite mode
	set_sprite_data(0, 8, TileLabel);
	set_sprite_tile(0, 0);
	move_sprite(0, 75, 75); // move to middle of screen
	set_sprite_tile(1, 2); // second half the sprite to make 16x16 sprite
	move_sprite(1, 75 + 8, 75); // move second half of sprite right 
	SHOW_SPRITES;

	while (1) {
		if (changet < 127) {
			set_sprite_tile(0, 4);
			set_sprite_tile(1, 6);
		}
		else if (changet > 127) {
			set_sprite_tile(0, 0);
			set_sprite_tile(1, 2);
		}
		changet++;
		wait_vbl_done();
	}

}