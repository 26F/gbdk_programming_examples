UINT8 mapx = 0;
UINT8 mapy = 0;
UINT8 spox = 0;
UINT8 spoy = 0;
void main() {
  set_bkg_palette(0,1,paldat);
  set_bkg_data(0,1,grid);
  VBK_REG = 1;
  set_bkg_tiles(0,0,32,32,cdatr);
  VBK_REG = 0;
  set_bkg_tiles(0,0,32,32,gridmap);

  // sprites
  set_sprite_palette(0,1,tpaldat);
  SPRITES_8x8;
  set_sprite_data(0,0, tile);
  set_sprite_tile(0,0);
  set_sprite_prop(0,0);
  move_sprite(0,8,16);

  SHOW_BKG; 
  SHOW_SPRITES;
  DISPLAY_ON;
  while (1) {
    if (joypad() & J_UP) {
      mapy -= 4;
    }
    if (joypad() & J_DOWN) {
      mapy += 4;
    }
    if (joypad() & J_LEFT) {
      mapx -= 4;
    }
    if (joypad() & J_RIGHT) {
      mapx += 4;
    }
    if (joypad() & J_A) {
      spox += 8;
    }
    if (joypad() & J_B) {
      spoy += 8;
    }
    move_sprite(0,(8-mapx)+spox,(16-mapy)+spoy);
    SCY_REG = mapy; 
    SCX_REG = mapx;
    wait_vbl_done();
    wait_vbl_done();
    wait_vbl_done();
  }
}